#define BUS_SPACE_MAXADDR_32BIT 0xFFFFFFFF
#if defined(__amd64__) || defined(PAE)
#define BUS_SPACE_MAXADDR	(bus_addr_t)0xFFFFFFFFFFFFFFFFULL
#else
#define BUS_SPACE_MAXADDR	(bus_addr_t)0xFFFFFFFF
#endif

